public enum Regiao {
    COSTA_NORTE, COSTA_NORDESTE, COSTA_SUDESTE, COSTA_SUL,
    ARQUIPELAGOS_ILHAS_OCEANICAS, AREA_DE_ESTUARIOS, AREA_DE_MANGUEZAIS, APAS

}

//Costa Norte do Brasil
//Amazonas e Amapá: Áreas próximas à foz do Rio Amazonas, onde a mistura de águas fluviais e oceânicas cria um ecossistema único.


//Costa Nordeste
//Maranhão, Piauí, Ceará, Rio Grande do Norte, Paraíba, Pernambuco, Alagoas e Bahia: Regiões costeiras que incluem importantes recifes de coral e áreas de biodiversidade marinha.


//Costa Sudeste
//Espírito Santo, Rio de Janeiro e São Paulo: Áreas urbanas costeiras que enfrentam desafios de poluição e que possuem ecossistemas marinhos diversos.


//Costa Sul
//Paraná, Santa Catarina e Rio Grande do Sul: Regiões conhecidas por suas correntes marinhas frias e ecossistemas marinhos ricos em biodiversidade.


//Arquipélagos e Ilhas Oceânicas
//Fernando de Noronha, Arquipélago de Abrolhos e Atol das Rocas: Áreas de conservação marinha que são essenciais para a proteção de espécies ameaçadas e a manutenção da biodiversidade.


//Áreas de Estuários
//Regiões como a Baía de Guanabara (Rio de Janeiro) e a Baía de Todos os Santos (Bahia): Importantes áreas de mistura de águas doces e salgadas que são fundamentais para a reprodução de várias espécies marinhas.


//Áreas de Manguezais
//Regiões costeiras do Maranhão, Pará e outras áreas do norte e nordeste do Brasil: Ecossistemas críticos para a proteção contra erosão e habitat para diversas espécies de vida marinha.


//Áreas de Proteção Ambiental (APAs)
//APAs Marinhas como a APA de Cananéia-Iguape-Peruíbe e a APA Marinha do Litoral Norte: Regiões sob proteção especial devido à sua importância ecológica.

